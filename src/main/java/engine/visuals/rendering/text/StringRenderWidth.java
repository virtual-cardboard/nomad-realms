package engine.visuals.rendering.text;

/**
 * A class that represents a string and the width of that string when rendered.
 *
 * @author Lunkle
 */
class StringRenderWidth {

	String string;
	float width;

	public StringRenderWidth(String string, float width) {
		this.string = string;
		this.width = width;
	}

}
