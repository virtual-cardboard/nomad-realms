package engine.common.java;

public class Pair<A, B> {

	public A a;
	public B b;

	public Pair(A a, B b) {
		this.a = a;
		this.b = b;
	}

	public A a() {
		return a;
	}

	public B b() {
		return b;
	}

	public A x() {
		return a;
	}

	public B y() {
		return b;
	}

	public A first() {
		return a;
	}

	public B second() {
		return b;
	}

	public A left() {
		return a;
	}

	public B right() {
		return b;
	}

}
